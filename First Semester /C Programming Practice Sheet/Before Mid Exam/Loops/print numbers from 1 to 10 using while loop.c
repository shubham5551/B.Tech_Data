// Program to print numbers from 1 to 10 using while loop
#include <stdio.h>

int main() {
    int i = 1;  // initialization

    // while loop runs as long as condition is true
    while (i <= 10) {
        printf("%d\n", i);  // print the current value
        i++;  // increment by 1 each time
    }

    return 0;
}

